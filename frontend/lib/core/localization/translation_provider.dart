// Supported Languages
enum AppLanguage { en, hi, ta, te, bn, mr }

// Settings Model
class AppSettings {
  final AppLanguage language;
  final AppFontSize fontSize;
  final bool audioGuidance;

  AppSettings({
    required this.language,
    required this.fontSize,
    required this.audioGuidance,
  });

  AppSettings copyWith({
    AppLanguage? language,
    AppFontSize? fontSize,
    bool? audioGuidance,
  }) {
    return AppSettings(
      language: language ?? this.language,
      fontSize: fontSize ?? this.fontSize,
      audioGuidance: audioGuidance ?? this.audioGuidance,
    );
  }
}

// Settings Notifier using Hive for Offline persistence
class SettingsNotifier extends StateNotifier<AppSettings> {
  final Box _box;

  SettingsNotifier(this._box)
      : super(AppSettings(
          language: _getLangFromString(_box.get('lang', defaultValue: 'hi')),
          fontSize: _getFontSizeFromString(_box.get('font_size', defaultValue: 'normal')),
          audioGuidance: _box.get('audio_guidance', defaultValue: true),
        ));

  static AppLanguage _getLangFromString(String code) {
    return AppLanguage.values.firstWhere(
      (l) => l.name == code,
      orElse: () => AppLanguage.hi,
    );
  }

  static AppFontSize _getFontSizeFromString(String size) {
    return AppFontSize.values.firstWhere(
      (f) => f.name == size,
      orElse: () => AppFontSize.normal,
    );
  }

  void setLanguage(AppLanguage language) {
    _box.put('lang', language.name);
    state = state.copyWith(language: language);
  }

  void setFontSize(AppFontSize fontSize) {
    _box.put('font_size', fontSize.name);
    state = state.copyWith(fontSize: fontSize);
  }

  void setAudioGuidance(bool enabled) {
    _box.put('audio_guidance', enabled);
    state = state.copyWith(audioGuidance: enabled);
  }
}

// Providers
final settingsBoxProvider = Provider<Box>((ref) => Hive.box('settings'));

final settingsProvider = StateNotifierProvider<SettingsNotifier, AppSettings>((ref) {
  final box = ref.read(settingsBoxProvider);
  return SettingsNotifier(box);
});

